export function applyTheme(preset: ThemePreset, isDark: boolean) {
  const root = document.documentElement;
  
  // Toggle the dark/light class for Tailwind
  root.classList.toggle('dark', isDark);
  root.classList.toggle('light', !isDark);
  root.setAttribute('data-theme-preset', preset.name);
  
  // CRITICAL: Write into the <style> tag, NOT inline styles.
  // Inline styles (element.style) have higher specificity than any class/rule
  // and override .dark { } class-based rules, breaking the toggle.
  // By updating the stylesheet, the cascade works correctly.
  let styleEl = document.getElementById('gt-theme-vars') as HTMLStyleElement | null;
  if (!styleEl) {
    styleEl = document.createElement('style');
    styleEl.id = 'gt-theme-vars';
    document.head.appendChild(styleEl);
  }
  
  const lightVars = Object.entries(preset.variables.light)
    .map(([k, v]) => `  ${k}: ${v};`)
    .join('\n');
  const darkVars = Object.entries(preset.variables.dark)
    .map(([k, v]) => `  ${k}: ${v};`)
    .join('\n');
  
  styleEl.textContent = `:root {\n${lightVars}\n}\n.dark {\n${darkVars}\n}`;
  
  // Remove any stale inline styles that may have been set previously
  const staleKeys = [
    '--background','--foreground','--card','--card-foreground',
    '--popover','--popover-foreground','--primary','--primary-foreground',
    '--secondary','--secondary-foreground','--muted','--muted-foreground',
    '--accent','--accent-foreground','--destructive','--destructive-foreground',
    '--border','--input','--ring',
  ];
  staleKeys.forEach(k => root.style.removeProperty(k));
  
  try { 
    localStorage.setItem('gt-theme', isDark ? 'dark' : 'light'); 
    localStorage.setItem('gt-theme-preset', preset.name);
  } catch {}
}

export function getThemeByName(name: string): ThemePreset | undefined {
  return THEME_PRESETS.find(t => t.name.toLowerCase() === name.toLowerCase());
}

/** Auto-apply saved theme preset on page load */
export function initTheme(): void {
  try {
    const savedPresetName = localStorage.getItem('gt-theme-preset') || 'Midnight';
    const savedMode = localStorage.getItem('gt-theme') || 'dark';
    const preset = getThemeByName(savedPresetName) || THEME_PRESETS[0];
    applyTheme(preset, savedMode === 'dark');
  } catch (err) {
    console.warn('[grodextt] theme init failed', err);
  }
}
