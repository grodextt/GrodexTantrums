export function applyTheme(preset: ThemePreset, isDark: boolean) {
  const root = document.documentElement;
  const vars = isDark ? preset.variables.dark : preset.variables.light;
  Object.entries(vars).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
}

export function getThemeByName(name: string): ThemePreset | undefined {
  return THEME_PRESETS.find(t => t.name === name);
}
